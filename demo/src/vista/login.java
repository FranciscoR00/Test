    private void isLabelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_isLabelMousePressed
       
        SqlUsuarios modSql= new SqlUsuarios();
        usuarios mod = new usuarios();
        
        String passStr = new String(Passwrd.getPassword());
        
        if (!username.getText().equals("") && !Passwrd.equals("") || !String.valueOf(Passwrd.getPassword()).equals("********")&& !username.getText().equals("Ingrese nombre de usuario")) {
            String nuevoPass= hash.sha1(passStr);
            
            mod.setUsuario(username.getText());
            mod.setPassword(nuevoPass);
            
            if(modSql.login(mod)){
                new Personaje().setVisible(true);
                this.dispose();
            } else{
                JOptionPane.showMessageDialog(null,"Datos Incorrectos");
            }
            
        } else{
            JOptionPane.showMessageDialog(null,"Debe ingresar datos, Aventurero (CRINGE)");
        }
        
    }//GEN-LAST:event_isLabelMousePressed

